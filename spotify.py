def get_musicbrainz_artist_details(artist_name, artist_cache):
    """
    Fetches artist metadata (type, country, career dates) from MusicBrainz.
    Handles API rate limits, uses a cache, and returns default values if no information is found.
    """
    cache_key = artist_name
    if cache_key in artist_cache:
        return artist_cache[cache_key]

    # Take the first artist if there's a comma-separated list for lookup simplicity
    primary_artist = artist_name.split(',')[0].strip()

    retries = 3
    for attempt in range(retries):
        try:
            time.sleep(1) # Adhere to MusicBrainz API rate limit
            result = mb.search_artists(artist=primary_artist, limit=1)

            artist_type, country, begin_year, end_year, is_active, has_data = 'unknown', 'unknown', np.nan, np.nan, False, 0

            if result and result['artist-list']:
                artist = result['artist-list'][0]
                artist_type = artist.get('type', 'unknown')
                country = artist.get('country', 'unknown')

                if 'life-span' in artist:
                    begin_date = artist['life-span'].get('begin')
                    if begin_date:
                        try:
                            begin_year = int(begin_date.split('-')[0])
                        except ValueError:
                            pass
                    end_date = artist['life-span'].get('end')
                    if end_date:
                        try:
                            end_year = int(end_date.split('-')[0])
                        except ValueError:
                            pass
                # An artist is considered active if no end date or end date is in the future/current year, and a begin year is known.
                is_active = (pd.isna(end_year) or (not pd.isna(end_year) and end_year >= pd.Timestamp.now().year)) and not pd.isna(begin_year)
                has_data = 1

            artist_cache[cache_key] = (artist_type, country, begin_year, end_year, is_active, has_data)
            return artist_type, country, begin_year, end_year, is_active, has_data

        except mb.WebServiceError as e:
            print(f"MusicBrainz API error for artist {primary_artist} (attempt {attempt+1}/{retries}): {e}")
            if attempt < retries - 1:
                time.sleep(5)
            else:
                artist_cache[cache_key] = ('unknown', 'unknown', np.nan, np.nan, False, 0)
                return 'unknown', 'unknown', np.nan, np.nan, False, 0
        except Exception as e:
            print(f"An unexpected error occurred for artist {primary_artist}: {e}")
            artist_cache[cache_key] = ('unknown', 'unknown', np.nan, np.nan, False, 0)
            return 'unknown', 'unknown', np.nan, np.nan, False, 0
    artist_cache[cache_key] = ('unknown', 'unknown', np.nan, np.nan, False, 0)
    return 'unknown', 'unknown', np.nan, np.nan, False, 0

