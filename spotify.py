def map_genre_to_super_genre(genre):
    genre = str(genre).lower() # Приводим к нижнему регистру для сравнения

    # Категория 1: Electronic / Pop (Электронная / Поп-музыка)
    electronic_pop_genres = [
        'pop', 'dance pop', 'electropop', 'pop rap', 'indie pop', 'synth-pop',
        'dance', 'electronic', 'edm', 'house', 'techno', 'trance', 'dubstep',
        'drum and bass', 'electro', 'synthwave', 'disco', 'k-pop', 'j-pop',
        'ambient pop', 'vaporwave'
    ]

    # Категория 2: Rock / Alternative (Рок / Альтернатива)
    rock_alternative_genres = [
        'rock', 'alternative rock', 'indie rock', 'punk', 'metal', 'hard rock',
        'classic rock', 'progressive rock', 'new wave', 'post-punk', 'grunge',
        'folk rock', 'alt-metal', 'punk rock', 'heavy metal', 'alternative'
    ]

    # Категория 3: Hip-Hop / R&B / Soul (Хип-Хоп / R&B / Соул)
    hiphop_rnb_soul_genres = [
        'hip hop', 'rap', 'r&b', 'soul', 'trap', 'gangsta rap', 'conscious hip hop',
        'g-funk', 'neo soul', 'funk', 'old school hip hop'
    ]

    # Категория 4: Acoustic / Folk / Country (Акустика / Фолк / Кантри)
    acoustic_folk_country_genres = [
        'acoustic', 'folk', 'country', 'blues', 'jazz', 'singer-songwriter',
        'americana', 'bluegrass', 'country pop', 'gospel'
    ]

    if any(g in genre for g in electronic_pop_genres):
        return 'Electronic/Pop'
    elif any(g in genre for g in rock_alternative_genres):
        return 'Rock/Alternative'
    elif any(g in genre for g in hiphop_rnb_soul_genres):
        return 'Hip-Hop/R&B/Soul'
    elif any(g in genre for g in acoustic_folk_country_genres):
        return 'Acoustic/Folk/Country'
    else:
        # Категория 5: World / Classical / Other (Мировая / Классическая / Прочее)
        return 'World/Classical/Other'

