# --- Cache for Album Information ---
CACHE_FILE = 'musicbrainz_cache.json'

def load_cache():
    """Loads the album cache from a JSON file."""
    if os.path.exists(CACHE_FILE):
        with open(CACHE_FILE, 'r') as f:
            loaded_cache = json.load(f)
            # Convert string 'nan' back to np.nan if necessary (None in JSON -> np.nan in Python)
            return {k: (v[0], v[1], v[2], (np.nan if v[3] is None else v[3]), v[4]) for k, v in loaded_cache.items()}
    return {}

def save_cache(cache_data):
    """Saves the album cache to a JSON file."""
    # Convert np.nan to None for JSON serialization
    serializable_cache = {k: (v[0], v[1], v[2], (None if pd.isna(v[3]) else v[3]), v[4]) for k, v in cache_data.items()}
    with open(CACHE_FILE, 'w') as f:
        json.dump(serializable_cache, f, indent=4)


def get_musicbrainz_album_info(artist_name, album_title, cache):
    """
    Fetches release metadata (country, status, label, year) for an album from MusicBrainz.
    Handles API rate limits, uses a cache, and returns default values if no information is found.
    """
    cache_key = f"{artist_name}|{album_title}"

    if cache_key in cache:
        return cache[cache_key]

    retries = 3
    for attempt in range(retries):
        try:
            time.sleep(1) # Adhere to MusicBrainz API rate limit (1 request per second)
            result = mb.search_releases(artist=artist_name, release=album_title, limit=1)

            country, status, label, year, has_data = 'unknown', 'unknown', 'unknown', np.nan, 0

            if result and result['release-list']:
                release = result['release-list'][0]
                country = release.get('country', 'unknown')
                status = release.get('status', 'unknown')

                if 'label-info' in release and release['label-info']:
                    label = release['label-info'][0].get('label', {}).get('name', 'unknown')

                if 'date' in release and release['date']:
                    try:
                        year = int(release['date'].split('-')[0])
                    except ValueError:
                        pass # year remains NaN if parsing fails
                has_data = 1 # Data found

            # Store result in cache before returning
            cache[cache_key] = (country, status, label, year, has_data)
            return country, status, label, year, has_data

        except mb.WebServiceError as e:
            print(f"MusicBrainz API error for {artist_name} - {album_title} (attempt {attempt+1}/{retries}): {e}")
            if attempt < retries - 1:
                time.sleep(5) # Wait longer before retrying
            else:
                # After all retries fail, store and return defaults
                cache[cache_key] = ('unknown', 'unknown', 'unknown', np.nan, 0)
                return 'unknown', 'unknown', 'unknown', np.nan, 0
        except Exception as e:
            print(f"An unexpected error occurred for {artist_name} - {album_title}: {e}")
            # Store and return defaults for unexpected errors
            cache[cache_key] = ('unknown', 'unknown', 'unknown', np.nan, 0)
            return 'unknown', 'unknown', 'unknown', np.nan, 0

    # Fallback if all retries fail
    cache[cache_key] = ('unknown', 'unknown', 'unknown', np.nan, 0)
    return 'unknown', 'unknown', 'unknown', np.nan, 0


